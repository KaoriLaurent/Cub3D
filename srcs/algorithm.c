t_position	next_intersection(short angle, t_player p, short r)
{
	t_position	pos;
	
	pos.x = p.x + (cos(degtorad(angle)) * (r / cos(angle)));
	pos.y = p.y - (sin(degtorad(angle)) * (r / cose(angle)));
	return (pos);
}

